/* ====================================================================
 *  内部绘图基元
 * ==================================================================== */

/**
 * @brief 确保 canvas 指针有效 (lazy get)
 */
static int osd_ensure_canvas(osd_ctx_t *ctx)
{
    if (!ctx || !ctx->canvas_ready) {
        return -1;
    }
    return 0;
}

static int osd_refresh_canvas(osd_ctx_t *ctx)
{
    ot_rgn_canvas_info info;

    if (!ctx) return -1;

    if (ss_mpi_rgn_get_canvas_info(ctx->rgn_handle, &info) != 0) {
        DBG_ERROR("OSD", "get_canvas_info failed");
        return -1;
    }
    ctx->canvas       = (uint8_t *)info.virt_addr;
    ctx->stride       = info.stride;
    ctx->canvas_ready = true;
    return 0;
}

static void osd_set_pixel(osd_ctx_t *ctx, int x, int y, osd_color_t c)
{
    uint16_t *row;

    if (x < 0 || y < 0 ||
        (uint16_t)x >= ctx->cfg.width ||
        (uint16_t)y >= ctx->cfg.height) {
        return;
    }

    row = (uint16_t *)(ctx->canvas + (uint32_t)y * ctx->stride);
    row[x] = c;
}

static void osd_draw_hline(osd_ctx_t *ctx, int x, int y, int len, osd_color_t c)
{
    int i;
    for (i = 0; i < len; i++) {
        osd_set_pixel(ctx, x + i, y, c);
    }
}

static void osd_draw_vline(osd_ctx_t *ctx, int x, int y, int len, osd_color_t c)
{
    int i;
    for (i = 0; i < len; i++) {
        osd_set_pixel(ctx, x, y + i, c);
    }
}

/* ====================================================================
 *  颜色工具
 * ==================================================================== */

osd_color_t osd_rgb(uint8_t r, uint8_t g, uint8_t b)
{
    return MAKE_ARGB1555(1,
                         (r & 0xF8) >> 3,
                         (g & 0xF8) >> 3,
                         (b & 0xF8) >> 3);
}

osd_color_t osd_rgba(uint8_t r, uint8_t g, uint8_t b, uint8_t a)
{
    return MAKE_ARGB1555(a > 127 ? 1 : 0,
                         (r & 0xF8) >> 3,
                         (g & 0xF8) >> 3,
                         (b & 0xF8) >> 3);
}

/* ====================================================================
 *  生命周期
 * ==================================================================== */

static osd_ctx_t *osd_create(const osd_cfg_t *cfg)
{
    osd_ctx_t   *ctx;
    ot_rgn_attr  attr;
    ot_rgn_chn_attr chn_attr;
    td_s32       ret;

    if (!cfg) return NULL;

    /* 参数对齐校验 */
    if (cfg->width < 2 || cfg->height < 2) {
        DBG_ERROR("OSD", "canvas size too small: %ux%u", cfg->width, cfg->height);
        return NULL;
    }

    ctx = calloc(1, sizeof(*ctx));
    if (!ctx) return NULL;

    /* handle 范围校验 */
    if (cfg->handle >= 8) {
        DBG_ERROR("OSD", "handle %u out of range [0,7]", cfg->handle);
        free(ctx);
        return NULL;
    }

    ctx->cfg          = *cfg;
    ctx->rgn_handle   = cfg->handle;
    ctx->canvas_ready = false;
    ctx->visible      = true;

    ctx->mpp_chn.mod_id = OT_ID_VENC;
    ctx->mpp_chn.dev_id = cfg->venc_dev;
    ctx->mpp_chn.chn_id = cfg->venc_chn;

    /* 1. 创建 OVERLAY region */
    (void)memset(&attr, 0, sizeof(attr));
    attr.type                          = OT_RGN_OVERLAY;
    attr.attr.overlay.pixel_format     = OT_PIXEL_FORMAT_ARGB_1555;
    attr.attr.overlay.size.width       = cfg->width;
    attr.attr.overlay.size.height      = cfg->height;
    attr.attr.overlay.canvas_num       = 2;  /* double buffer anti-tear */
    attr.attr.overlay.bg_color         = ARGB1555_TRANSPARENT;

    /* 默认 CLUT (ARGB1555 不使用 CLUT, 但必须填) */
    attr.attr.overlay.clut[0]          = 0x0;
    attr.attr.overlay.clut[1]          = 0xffffffff;

    ret = ss_mpi_rgn_create(ctx->rgn_handle, &attr);
    if (ret != 0) {
        DBG_ERROR("OSD", "rgn_create h%d failed: %#x", ctx->rgn_handle, ret);
        free(ctx);
        return NULL;
    }

    /* 2. 绑定到 VENC 通道 */
    (void)memset(&chn_attr, 0, sizeof(chn_attr));
    chn_attr.is_show                    = TD_TRUE;
    chn_attr.type                       = OT_RGN_OVERLAY;
    chn_attr.attr.overlay_chn.point.x   = cfg->x;
    chn_attr.attr.overlay_chn.point.y   = cfg->y;
    chn_attr.attr.overlay_chn.fg_alpha  = cfg->fg_alpha;
    chn_attr.attr.overlay_chn.bg_alpha  = cfg->bg_alpha;
    chn_attr.attr.overlay_chn.layer     = cfg->layer;

    ret = ss_mpi_rgn_attach_to_chn(ctx->rgn_handle, &ctx->mpp_chn, &chn_attr);
    if (ret != 0) {
        DBG_ERROR("OSD", "rgn_attach_to_chn h%d failed: %#x", ctx->rgn_handle, ret);
        ss_mpi_rgn_destroy(ctx->rgn_handle);
        free(ctx);
        return NULL;
    }

    /* 3. 获取初始化 canvas */
    if (osd_refresh_canvas(ctx) != 0) {
        ss_mpi_rgn_detach_from_chn(ctx->rgn_handle, &ctx->mpp_chn);
        ss_mpi_rgn_destroy(ctx->rgn_handle);
        free(ctx);
        return NULL;
    }

    /* 4. 初始清空 */
    osd_clear(ctx);

    DBG_LOG("OSD", "created h%d %ux%u at (%u,%u) chn=%d",
            ctx->rgn_handle,
            cfg->width, cfg->height, cfg->x, cfg->y, cfg->venc_chn);
    return ctx;
}

static void osd_destroy(osd_ctx_t *ctx)
{
    if (!ctx) return;

    if (ctx->rgn_handle >= 0) {
        td_s32 old_handle = ctx->rgn_handle;
        ss_mpi_rgn_detach_from_chn(ctx->rgn_handle, &ctx->mpp_chn);
        ss_mpi_rgn_destroy(ctx->rgn_handle);
        ctx->rgn_handle = -1;
        DBG_LOG("OSD", "destroyed h%d", old_handle);
    }

    free(ctx);
}

/* ====================================================================
 *  显隐
 * ==================================================================== */

static int osd_show(osd_ctx_t *ctx)
{
    ot_rgn_chn_attr attr;
    td_s32 ret;

    if (!ctx || ctx->rgn_handle < 0) return -1;
    if (ctx->visible) return 0; /* 已经可见 */

    (void)memset(&attr, 0, sizeof(attr));
    attr.is_show                    = TD_TRUE;
    attr.type                       = OT_RGN_OVERLAY;
    attr.attr.overlay_chn.point.x   = ctx->cfg.x;
    attr.attr.overlay_chn.point.y   = ctx->cfg.y;
    attr.attr.overlay_chn.fg_alpha  = ctx->cfg.fg_alpha;
    attr.attr.overlay_chn.bg_alpha  = ctx->cfg.bg_alpha;
    attr.attr.overlay_chn.layer     = ctx->cfg.layer;

    ret = ss_mpi_rgn_set_chn_display_attr(ctx->rgn_handle, &ctx->mpp_chn, &attr);
    if (ret != 0) {
        DBG_ERROR("OSD", "show h%d failed: %#x", ctx->rgn_handle, ret);
        return -1;
    }

    ctx->visible = true;
    DBG_LOG("OSD", "shown h%d", ctx->rgn_handle);
    return 0;
}

static int osd_hide(osd_ctx_t *ctx)
{
    ot_rgn_chn_attr attr;
    td_s32 ret;

    if (!ctx || ctx->rgn_handle < 0) return -1;
    if (!ctx->visible) return 0; /* 已经隐藏 */

    (void)memset(&attr, 0, sizeof(attr));
    attr.is_show                    = TD_FALSE;
    attr.type                       = OT_RGN_OVERLAY;
    attr.attr.overlay_chn.point.x   = ctx->cfg.x;
    attr.attr.overlay_chn.point.y   = ctx->cfg.y;
    attr.attr.overlay_chn.fg_alpha  = ctx->cfg.fg_alpha;
    attr.attr.overlay_chn.bg_alpha  = ctx->cfg.bg_alpha;
    attr.attr.overlay_chn.layer     = ctx->cfg.layer;

    ret = ss_mpi_rgn_set_chn_display_attr(ctx->rgn_handle, &ctx->mpp_chn, &attr);
    if (ret != 0) {
        DBG_ERROR("OSD", "hide h%d failed: %#x", ctx->rgn_handle, ret);
        return -1;
    }

    ctx->visible = false;
    DBG_LOG("OSD", "hidden h%d", ctx->rgn_handle);
    return 0;
}

/* ====================================================================
 *  绘图操作
 * ==================================================================== */

static int osd_clear(osd_ctx_t *ctx)
{
    uint32_t size;

    if (osd_ensure_canvas(ctx) != 0) return -1;

    size = (uint32_t)ctx->cfg.height * ctx->stride;
    (void)memset(ctx->canvas, 0, size);
    return 0;
}

static int osd_draw_rect(osd_ctx_t *ctx, uint16_t x, uint16_t y,
                  uint16_t w, uint16_t h, uint8_t thickness, osd_color_t color)
{
    int i;
    if (osd_ensure_canvas(ctx) != 0) return -1;
    if (thickness == 0) thickness = 1;

    for (i = 0; i < (int)thickness; i++) {
        osd_draw_hline(ctx, x, y + i,            w, color);             /* top */
        osd_draw_hline(ctx, x, y + h - 1 - i,    w, color);             /* bottom */
        osd_draw_vline(ctx, x + i,     y,        h, color);             /* left */
        osd_draw_vline(ctx, x + w - 1 - i, y,    h, color);             /* right */
    }
    return 0;
}

static int osd_draw_text(osd_ctx_t *ctx, int16_t x, int16_t y,
                  const char *text, osd_color_t color, uint8_t scale)
{
    const char *p;
    int col, row;
    int cx, cy;
    int sx, sy;

    if (osd_ensure_canvas(ctx) != 0) return -1;
    if (!text || scale == 0) return -1;
    if (scale > 4) scale = 4;

    cx = x;
    cy = y;
    for (p = text; *p; p++) {
        uint8_t ch = (uint8_t)*p;

        if (ch == '\n') {
            cx = x;
            cy += 16 * (int)scale;
            continue;
        }
        if (ch < 32 || ch > 126) {
            cx += 8 * (int)scale;
            continue;
        }

        const uint8_t *glyph = g_font_8x16[ch - 32];

        for (row = 0; row < 16; row++) {
            uint8_t bits = glyph[row];
            for (col = 0; col < 8; col++) {
                if (bits & (0x80 >> col)) {
                    /* 放大: 每个像素点描 scale x scale 块 */
                    for (sy = 0; sy < (int)scale; sy++) {
                        for (sx = 0; sx < (int)scale; sx++) {
                            osd_set_pixel(ctx,
                                          cx + col * scale + sx,
                                          cy + row * scale + sy,
                                          color);
                        }
                    }
                }
            }
        }
        cx += 8 * (int)scale;
    }
    return 0;
}

static int osd_draw_bitmap(osd_ctx_t *ctx, int16_t x, int16_t y,
                    const uint16_t *data, uint16_t w, uint16_t h)
{
    int row, col;

    if (osd_ensure_canvas(ctx) != 0) return -1;
    if (!data) return -1;

    for (row = 0; row < (int)h; row++) {
        int dst_y = y + row;
        uint16_t *dst_row;

        if (dst_y < 0 || (uint16_t)dst_y >= ctx->cfg.height) continue;

        dst_row = (uint16_t *)(ctx->canvas + (uint32_t)dst_y * ctx->stride);
        for (col = 0; col < (int)w; col++) {
            int dst_x = x + col;
            if (dst_x < 0 || (uint16_t)dst_x >= ctx->cfg.width) continue;
            dst_row[dst_x] = data[row * w + col];
        }
    }
    return 0;
}

static int osd_update(osd_ctx_t *ctx)
{
    if (!ctx) return -1;
    if (!ctx->canvas_ready) return -1;

    /* 推送 canvas 到硬件 */
    if (ss_mpi_rgn_update_canvas(ctx->rgn_handle) != 0) {
        DBG_ERROR("OSD", "update_canvas failed");
        return -1;
    }

    /* 刷新 canvas 指针 (双缓冲轮换) */
    return osd_refresh_canvas(ctx);
}

/* ====================================================================
 *  时间子模块 — 每实例自带线程
 * ==================================================================== */

struct time_osd_ctx {
    osd_ctx_t  *osd;
    thread_t    thread;
    uint8_t     scale;
    osd_color_t color;
    volatile int running;
};

static void *time_osd_thread(void *arg)
{
    time_osd_t      *ctx = (time_osd_t *)arg;
    struct timespec  start, end, sleep_ts;
    long             elapsed_ms, sleep_ms;
    long             sec_diff, nsec_diff;

    thread_set_name("osd_time");

    while (ctx->running) {
        clock_gettime(CLOCK_MONOTONIC, &start);
        time_osd_update(ctx);
        clock_gettime(CLOCK_MONOTONIC, &end);

        /* 计算本次 update 耗时 (ms) */
        sec_diff  = end.tv_sec - start.tv_sec;
        nsec_diff = end.tv_nsec - start.tv_nsec;
        if (nsec_diff < 0) {
            sec_diff -= 1;
            nsec_diff += 1000000000L;
        }
        elapsed_ms = sec_diff * 1000 + nsec_diff / 1000000;

        /* 还需等待 1 秒减去已花时间 */
        sleep_ms = 1000 - elapsed_ms;
        if (sleep_ms < 0) sleep_ms = 0;

        if (!ctx->running) break;

        sleep_ts.tv_sec  = sleep_ms / 1000;
        sleep_ts.tv_nsec = (sleep_ms % 1000) * 1000000L;
        clock_nanosleep(CLOCK_MONOTONIC, 0, &sleep_ts, NULL);
    }
    return NULL;
}

time_osd_t *time_osd_create(uint8_t venc_dev, uint8_t venc_chn,
                             uint8_t handle, uint16_t x, uint16_t y,
                             uint8_t scale, osd_color_t color)
{
    osd_cfg_t   cfg;
    time_osd_t *t;

    if (scale == 0) scale = 1;
    if (scale > 4) scale = 4;

    t = (time_osd_t *)calloc(1, sizeof(*t));
    if (!t) return NULL;

    /* 创建底层 OSD */
    memset(&cfg, 0, sizeof(cfg));
    cfg.width     = (uint16_t)(19 * 8 * scale);
    cfg.height    = (uint16_t)(16 * scale);
    cfg.x         = x;
    cfg.y         = y;
    cfg.fg_alpha  = 255;
    cfg.bg_alpha  = 0;
    cfg.layer     = 7;
    cfg.venc_dev  = venc_dev;
    cfg.venc_chn  = venc_chn;
    cfg.handle    = handle;

    t->osd = osd_create(&cfg);
    if (!t->osd) {
        free(t);
        return NULL;
    }

    t->scale   = scale;
    t->color   = color;
    t->running = 1;

    /* 先画一帧初始时间 */
    time_osd_update(t);

    /* 启动独立刷新线程 */
    if (thread_create(&t->thread, "osd_time", 8192,
                      time_osd_thread, t) != TD_SUCCESS) {
        t->running = 0;
        osd_destroy(t->osd);
        free(t);
        return NULL;
    }

    return t;
}

void time_osd_destroy(time_osd_t *ctx)
{
    if (!ctx) return;

    ctx->running = 0;
    thread_join(ctx->thread);

    if (ctx->osd) {
        osd_destroy(ctx->osd);
        ctx->osd = NULL;
    }
    free(ctx);
}

int time_osd_update(time_osd_t *ctx)
{
    time_t now;
    struct tm tm;
    char buf[32];

    if (!ctx || !ctx->osd) return -1;

    time(&now);
    localtime_r(&now, &tm);
    snprintf(buf, sizeof(buf), "%04d-%02d-%02d %02d:%02d:%02d",
             tm.tm_year + 1900, tm.tm_mon + 1, tm.tm_mday,
             tm.tm_hour, tm.tm_min, tm.tm_sec);

    osd_clear(ctx->osd);
    osd_draw_text(ctx->osd, 0, 0, buf, ctx->color, ctx->scale);
    return osd_update(ctx->osd);
}

int time_osd_show(time_osd_t *ctx)
{
    return (ctx && ctx->osd) ? osd_show(ctx->osd) : -1;
}

int time_osd_hide(time_osd_t *ctx)
{
    return (ctx && ctx->osd) ? osd_hide(ctx->osd) : -1;
}

