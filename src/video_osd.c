/* ====================================================================
 *  ARGB1555 颜色编码
 *
 *  bit15       = Alpha  (1=opaque, 0=transparent)
 *  bits[14:10] = Red    (0-31)
 *  bits[9:5]   = Green  (0-31)
 *  bits[4:0]   = Blue   (0-31)
 * ==================================================================== */

#define ARGB1555_A_POS   15
#define ARGB1555_R_POS   10
#define ARGB1555_G_POS   5
#define ARGB1555_B_POS   0

#define MAKE_ARGB1555(a, r, g, b) \
    ((((a) & 1) << ARGB1555_A_POS) | \
     (((r) & 0x1F) << ARGB1555_R_POS) | \
     (((g) & 0x1F) << ARGB1555_G_POS) | \
     (((b) & 0x1F) << ARGB1555_B_POS))

#define ARGB1555_TRANSPARENT  MAKE_ARGB1555(0, 0, 0, 0)

